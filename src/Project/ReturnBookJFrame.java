    public final void showRcd(String temp){
        try{
            Connection con=Connector.DBConnector();
            String qry="select * from record_info";
            Statement stmt=con.createStatement();
            ResultSet rs=stmt.executeQuery(qry);
            while(rs.next()){
                String sname=rs.getString(3);
                String bname=rs.getString(4);
                String edtn=rs.getString(5);
                String lge=rs.getString(6);
                String issuedt=rs.getString(7);
                String duedt=rs.getString(8);
                String sts=rs.getString(10);
                if(sname.equals(temp) && sts.equals("Issued")){
                    String tbData[]={bname,edtn,lge,issuedt,duedt};
                    DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
                    model.addRow(tbData);
                }
            }
        }
        catch(Exception ex){
            System.out.println(ex.getMessage());
        }
    }
    
    public void getRecordData(int n){
        DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
        txtBName.setText(model.getValueAt(n, 0).toString());
        txtEdition.setText(model.getValueAt(n, 1).toString());
        txtLanguage.setText(model.getValueAt(n, 2).toString());
    }
