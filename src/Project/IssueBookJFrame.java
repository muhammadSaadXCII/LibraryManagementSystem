    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(txtAName.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(IssueBookJFrame.this,"Enter Data Correctly.");
            txtAName.grabFocus();
            return;
        }
        if(txtSName.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(IssueBookJFrame.this,"Enter Data Correctly.");
            txtSName.grabFocus();
            return;
        }
        if(txtBName.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(IssueBookJFrame.this,"Enter Data Correctly.");
            txtBName.grabFocus();
            return;
        }
        if(txtEdition.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(IssueBookJFrame.this,"Enter Data Correctly.");
            txtEdition.grabFocus();
            return;
        }
        if(txtLanguage.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(IssueBookJFrame.this,"Enter Data Correctly.");
            txtLanguage.grabFocus();
            return;
        }
        if(dtDueDate.getDate()==null){
            JOptionPane.showMessageDialog(IssueBookJFrame.this, "Please select a date.");
            return;
        }
        Book bk=new Book();
        String aname=txtAName.getText();
        String sname=txtSName.getText();
        String bname=txtBName.getText();
        String edtn=txtEdition.getText();
        String lge=txtLanguage.getText();
        LocalDate ld=LocalDate.now();
        DateTimeFormatter dtf=DateTimeFormatter.ofPattern("dd-MM-yyyy");
        String issuedt=ld.format(dtf);
        Date n=dtDueDate.getDate();
        String duedt=changeDateFormat(n);
        bk.issueBook(aname, sname, bname, edtn, lge, issuedt, duedt);
    }//GEN-LAST:event_jButton1ActionPerformed

