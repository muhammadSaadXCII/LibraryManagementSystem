    public void showOdr(){
        try{
            Connection con=Connector.DBConnector();
            String qry="select * from order_info";
            Statement stmt=con.createStatement();
            ResultSet rs=stmt.executeQuery(qry);
            while(rs.next()){
                String sname=rs.getString(2);
                String bname=rs.getString(3);
                String edtn=rs.getString(4);
                String lge=rs.getString(5);
                String sts=rs.getString(6);
                if(sts.equals("Pending...")){
                    String tbData[]={sname,bname,edtn,lge};
                    DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
                    model.addRow(tbData);
                }
            }
        }
        catch(Exception ex){
            System.out.println(ex.getMessage());
        }
    }
    
