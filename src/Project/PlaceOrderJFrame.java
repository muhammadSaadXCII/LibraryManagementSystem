    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Book bk=new Book();
        if(txtName.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(PlaceOrderJFrame.this,"Enter Data Correctly.");
            txtName.grabFocus();
            return;
        }
        if(txtBName.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(PlaceOrderJFrame.this,"Enter Data Correctly.");
            txtBName.grabFocus();
            return;
        }
        if(txtEdition.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(PlaceOrderJFrame.this,"Enter Data Correctly.");
            txtEdition.grabFocus();
            return;
        }
        if(txtLanguage.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(PlaceOrderJFrame.this,"Enter Data Correctly.");
            txtLanguage.grabFocus();
            return;
        }
        String name=txtName.getText();
        String bname=txtBName.getText();
        String edtn=txtEdition.getText();
        String lge=txtLanguage.getText();
        bk.placeOrder(name, bname, edtn, lge);
    }//GEN-LAST:event_jButton1ActionPerformed

