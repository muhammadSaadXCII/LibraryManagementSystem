    public void showBK(){
        try{
            Connection con=Connector.DBConnector();
            String qry="select * from book_info";
            Statement stmt=con.createStatement();
            ResultSet rs=stmt.executeQuery(qry);
            while(rs.next()){
                String bname=rs.getString(2);
                String aname=rs.getString(3);
                String edtn=rs.getString(4);
                String lge=rs.getString(5);
                String tbData[]={bname,aname,edtn,lge};
                DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
                model.addRow(tbData);
            }
        }
        catch(Exception ex){
            System.out.println(ex.getMessage());
        }
    }
    
    public void getBookData(int n){
        DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
        txtBName.setText(model.getValueAt(n, 0).toString());
        txtEdition.setText(model.getValueAt(n, 2).toString());
        txtLanguage.setText(model.getValueAt(n, 3).toString());
    }
